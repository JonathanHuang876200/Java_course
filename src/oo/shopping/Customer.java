package oo.shopping;

public class Customer {
	int price;
	int pay;
	int moneyback;

	public Customer() {
		this.price = price;
		this.pay = pay;
		this.moneyback = moneyback;
	}

	public int pay(int price) {
		pay = price;
		return pay;
	}

	public int moneyback(int price) {
		moneyback = price * 0;
		return moneyback;
	}

}
